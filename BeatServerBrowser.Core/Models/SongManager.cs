        #region // コマンド用メソッド
        private async void ShowDetail(BeatmapEntity beatmap)
        {
            if (this.IsOpen == true) {
                return;
            }
            try {
                var view = new SongDetail();
                view.DataContext = new SongDetailDialogViewModel() { Beatmap = beatmap };
                await DialogHost.Show(view, "SongDetail");
            }
            catch (Exception e) {
                Debug.WriteLine(e);
            }
            finally {
                this.IsOpen = false;
            }
            
        }
