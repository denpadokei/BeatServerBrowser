        /// <summary>曲詳細コマンド を取得、設定</summary>
        public DelegateCommand<BeatmapEntity> ShowDeailCommand => this.showDetailCommand_ ?? (this.showDetailCommand_ = new DelegateCommand<BeatmapEntity>(this.ShowDetail));
