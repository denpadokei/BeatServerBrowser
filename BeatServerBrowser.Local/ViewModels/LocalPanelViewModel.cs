        #region // コマンド
        /// <summary>表示コマンド を取得、設定</summary>
        private DelegateCommand showCommand_;
        /// <summary>表示コマンド を取得、設定</summary>
        public DelegateCommand ShowCommand => this.showCommand_ ?? (this.showCommand_ = new DelegateCommand(this.Show));
        #endregion
